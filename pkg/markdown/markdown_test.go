package markdown

import (
	"strings"
	"testing"
)

func TestConvertToGithubMarkdown(t *testing.T) {
	tests := []struct {
		name     string
		input    string
		expected string
	}{
		{
			name: "existing placeholders",
			input: `{% block page_title %}Hello{% endblock %}
This is on {{gcp_name_short}} with {{product_name_short}}.`,
			expected: `---
# AUTOGENERATED. DO NOT EDIT.
title: "Hello"
---
This is on Google Cloud with Config Connector.`,
		},
		{
			name: "new placeholders",
			input: `This is {{product_name}} with {{iam_name_short}}.
It uses {{crmapi_name}}.
It connects to {{billing_name}} accounts using {{billing_api}}.`,
			expected: `---
# AUTOGENERATED. DO NOT EDIT.
---
This is Config Connector with IAM.
It uses Cloud Resource Manager API.
It connects to Cloud Billing accounts using Cloud Billing API.`,
		},
		{
			name: "relative links conversion to docs.cloud.google.com",
			input: `<tr>
<td>Google Cloud Service Documentation</td>
<td><a href="/compute/docs/">/compute/docs/</a></td>
</tr>
<tr>
<td>Google Cloud REST Resource Documentation</td>
<td><a href="/compute/docs/reference/rest/beta/networkEndpointGroups">/compute/docs/reference/rest/beta/networkEndpointGroups</a></td>
</tr>
Note: refer to <a href="/config-connector/docs/troubleshooting">Troubleshoot Config Connector</a>.
Check [markdown link](/compute/docs/reference).
Multiple on same line: <a href="/path1">/path1</a> and <a href="/path2">/path2</a> but not normal text /compute/docs/.`,
			expected: `---
# AUTOGENERATED. DO NOT EDIT.
---
<tr>
<td>Google Cloud Service Documentation</td>
<td><a href="https://docs.cloud.google.com/compute/docs/">https://docs.cloud.google.com/compute/docs/</a></td>
</tr>
<tr>
<td>Google Cloud REST Resource Documentation</td>
<td><a href="https://docs.cloud.google.com/compute/docs/reference/rest/beta/networkEndpointGroups">https://docs.cloud.google.com/compute/docs/reference/rest/beta/networkEndpointGroups</a></td>
</tr>
Note: refer to <a href="https://docs.cloud.google.com/config-connector/docs/troubleshooting">Troubleshoot Config Connector</a>.
Check [markdown link](https://docs.cloud.google.com/compute/docs/reference).
Multiple on same line: <a href="https://docs.cloud.google.com/path1">https://docs.cloud.google.com/path1</a> and <a href="https://docs.cloud.google.com/path2">https://docs.cloud.google.com/path2</a> but not normal text /compute/docs/.`,
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			got := ConvertToGithubMarkdown(tc.input)
			if strings.TrimSpace(got) != strings.TrimSpace(tc.expected) {
				t.Errorf("expected:\n%s\ngot:\n%s", tc.expected, got)
			}
		})
	}
}

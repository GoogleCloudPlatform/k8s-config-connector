package markdown

import (
	"regexp"
	"strings"
)

// ConvertToGithubMarkdown converts Google-flavored markdown templates to Github-flavored markdown.
func ConvertToGithubMarkdown(content string) string {
	s := content

	// Remove google-specific boilerplate tags
	s = strings.ReplaceAll(s, "{# AUTOGENERATED. DO NOT EDIT. #}\n", "")
	s = strings.ReplaceAll(s, "{# AUTOGENERATED. DO NOT EDIT. #}", "")
	s = regexp.MustCompile(`(?m)^{% extends .*? %}\n*`).ReplaceAllString(s, "")
	s = regexp.MustCompile(`(?m)^{% block body %}\n*`).ReplaceAllString(s, "")
	s = regexp.MustCompile(`(?m)^{% endblock %}\n*`).ReplaceAllString(s, "")

	// Extract title if present
	title := ""
	titleMatch := regexp.MustCompile(`(?m)^{% block page_title %}(.*?){% endblock %}\n*`).FindStringSubmatch(s)
	if len(titleMatch) > 1 {
		title = titleMatch[1]
		s = regexp.MustCompile(`(?m)^{% block page_title %}(.*?){% endblock %}\n*`).ReplaceAllString(s, "")
	}

	// Replace placeholders
	s = strings.ReplaceAll(s, "{{gcp_name_short}}", "Google Cloud")
	s = strings.ReplaceAll(s, "{{product_name_short}}", "Config Connector")
	s = strings.ReplaceAll(s, "{{product_name}}", "Config Connector")
	s = strings.ReplaceAll(s, "{{iam_name_short}}", "IAM")
	s = strings.ReplaceAll(s, "{{crmapi_name}}", "Cloud Resource Manager API")
	s = strings.ReplaceAll(s, "{{billing_name}}", "Cloud Billing")
	s = strings.ReplaceAll(s, "{{billing_api}}", "Cloud Billing API")

	// Remove verbatim tags
	s = strings.ReplaceAll(s, "{% verbatim %}", "")
	s = strings.ReplaceAll(s, "{% endverbatim %}", "")

	// Remove excessive newlines
	s = regexp.MustCompile(`\n{3,}`).ReplaceAllString(s, "\n\n")

	s = strings.TrimSpace(s) + "\n"

	// Prepend front matter
	frontMatter := "---\n# AUTOGENERATED. DO NOT EDIT.\n"
	if title != "" {
		frontMatter += "title: \"" + title + "\"\n"
	}
	frontMatter += "---\n"

	return frontMatter + s
}

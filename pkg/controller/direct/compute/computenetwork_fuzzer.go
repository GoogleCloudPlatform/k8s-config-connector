// +tool:fuzz-gen
// proto.message: google.cloud.compute.v1.Network
// api.group: compute.cnrm.cloud.google.com

package compute

import (
	pb "cloud.google.com/go/compute/apiv1/computepb"
	"github.com/GoogleCloudPlatform/k8s-config-connector/pkg/fuzztesting"
)

func init() {
	fuzztesting.RegisterKRMFuzzer(computeNetworkFuzzer())
}

func computeNetworkFuzzer() fuzztesting.KRMFuzzer {
	f := fuzztesting.NewKRMTypedFuzzer(&pb.Network{},
		ComputeNetworkSpec_v1beta1_FromProto, ComputeNetworkSpec_v1beta1_ToProto,
		ComputeNetworkStatus_v1beta1_FromProto, ComputeNetworkStatus_v1beta1_ToProto,
	)

	// Spec fields
	f.SpecField(".auto_create_subnetworks")
	f.SpecField(".description")
	f.SpecField(".enable_ula_internal_ipv6")
	f.SpecField(".internal_ipv6_range")
	f.SpecField(".mtu")
	f.SpecField(".network_firewall_policy_enforcement_order")
	f.SpecField(".routing_config.routing_mode")

	// Status fields
	// Note: Field names like ".gateway_i_pv4" and ".I_pv4_range" reflect the actual, somewhat surprising, GCP proto names
	// generated by the proto-to-go compiler (which converts IPv4/IPv6 into i_pv4/i_pv6/I_pv4/I_pv6 due to uppercase acronym handling).
	f.StatusField(".gateway_i_pv4")
	f.StatusField(".self_link")

	// Identity / Special fields
	f.Unimplemented_Identity(".name")

	// Unimplemented / Not Yet Triaged fields
	f.Unimplemented_NotYetTriaged(".I_pv4_range")
	f.Unimplemented_NotYetTriaged(".creation_timestamp")
	f.Unimplemented_NotYetTriaged(".firewall_policy")
	f.Unimplemented_NotYetTriaged(".id")
	f.Unimplemented_NotYetTriaged(".kind")
	f.Unimplemented_NotYetTriaged(".network_profile")
	f.Unimplemented_NotYetTriaged(".params")
	f.Unimplemented_NotYetTriaged(".peerings")
	f.Unimplemented_NotYetTriaged(".self_link_with_id")
	f.Unimplemented_NotYetTriaged(".subnetworks")

	// Nested routing_config fields that are not mapped
	f.Unimplemented_NotYetTriaged(".routing_config.bgp_always_compare_med")
	f.Unimplemented_NotYetTriaged(".routing_config.bgp_best_path_selection_mode")
	f.Unimplemented_NotYetTriaged(".routing_config.bgp_inter_region_cost")
	f.Unimplemented_NotYetTriaged(".routing_config.effective_bgp_always_compare_med")
	f.Unimplemented_NotYetTriaged(".routing_config.effective_bgp_inter_region_cost")

	f.FilterSpec = func(in *pb.Network) {
		if in.RoutingConfig != nil && in.RoutingConfig.RoutingMode == nil {
			in.RoutingConfig = nil
		}
	}

	f.FilterStatus = func(in *pb.Network) {
		in.RoutingConfig = nil
	}

	return f
}

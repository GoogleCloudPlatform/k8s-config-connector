var _ refsv1beta1.ExternalNormalizer = &ExecutionRef{}

// ExecutionRef defines the resource reference to WorkflowsExecution, which "External" field
// holds the GCP identifier for the KRM object.
type ExecutionRef struct {
	// A reference to an externally managed WorkflowsExecution resource.
	// Should be in the format "projects/{{projectID}}/locations/{{location}}/executions/{{executionID}}".
	External string `json:"external,omitempty"`

	// The name of a WorkflowsExecution resource.
	Name string `json:"name,omitempty"`

	// The namespace of a WorkflowsExecution resource.
	Namespace string `json:"namespace,omitempty"`
}

// New builds a NewExecutionRef from the Config Connector Execution object.
func NewExecutionRef(ctx context.Context, reader client.Reader, obj *WorkflowsExecution) (*ExecutionRef, error) {
	id := &ExecutionRef{}

	// Get Parent
	projectRef, err := refsv1beta1.ResolveProject(ctx, reader, obj.GetNamespace(), obj.Spec.ProjectRef)
	if err != nil {
		return nil, err
	}
	projectID := projectRef.ProjectID
	if projectID == "" {
		return nil, fmt.Errorf("cannot resolve project")
	}
	location := obj.Spec.Location
	_, workflow, err := workflow.ParseWorkflowsWorkflowExternal(obj.Spec.WorkflowRef.External)
	if err != nil {
		return nil, err
	}

	// Get desired ID
	resourceID := common.ValueOf(obj.Spec.ResourceID)

	// Use approved External
	externalRef := common.ValueOf(obj.Status.ExternalRef)
	if externalRef != "" {
		// Validate desired with actual
		actualParent, actualResourceID, err := ParseExecutionExternal(externalRef)
		if err != nil {
			return nil, err
		}
		if actualParent.ProjectID != projectID {
			return nil, fmt.Errorf("spec.projectRef changed, expect %s, got %s", actualParent.ProjectID, projectID)
		}
		if actualParent.Location != location {
			return nil, fmt.Errorf("spec.location changed, expect %s, got %s", actualParent.Location, location)
		}
		if resourceID != "" && actualResourceID != resourceID {
			return nil, fmt.Errorf("cannot reset `metadata.name` or `spec.resourceID` to %s, since it has already assigned to %s",
				resourceID, actualResourceID)
		}
		id.External = externalRef
		return id, nil
	}
	id.External = "projects/" + projectID + "/locations/" + location + "/workflows/" + workflow + "/executions/" + resourceID
	return id, nil
}

// ExecutionID returns the execution ID, a boolean indicating whether the execution ID is specified by user (or generated by service), and an error.
func (r *ExecutionRef) ExecutionID() (string, bool, error) {
	if r.External != "" {
		_, id, err := ParseExecutionExternal(r.External)
		if err != nil {
			return "", false, err
		}
		return id, id != "", nil
	}
	return "", false, fmt.Errorf("ExecutionRef not normalized to External form or not created from `New()`")
}

func (r *ExecutionRef) Parent() (string, error) {
	if r.External != "" {
		r.External = strings.TrimPrefix(r.External, "/")
		parent, _, err := ParseExecutionExternal(r.External)
		if err != nil {
			return "", err
		}
		return parent.String(), nil
	}
	return "", fmt.Errorf("BigQueryConnectionConnectionRef not normalized to External form or not created from `New()`")
}

// NormalizedExternal provision the "External" value for other resource that depends on WorkflowsExecution.
// If the "External" is given in the other resource's spec.WorkflowsExecutionRef, the given value will be used.
// Otherwise, the "Name" and "Namespace" will be used to query the actual WorkflowsExecution object from the cluster.
func (r *ExecutionRef) NormalizedExternal(ctx context.Context, reader client.Reader, otherNamespace string) (string, error) {
	if r.External != "" && r.Name != "" {
		return "", fmt.Errorf("cannot specify both name and external on %s reference", WorkflowsExecutionGVK.Kind)
	}
	// From given External
	if r.External != "" {
		if _, _, err := ParseExecutionExternal(r.External); err != nil {
			return "", err
		}
		return r.External, nil
	}

	// From the Config Connector object
	if r.Namespace == "" {
		r.Namespace = otherNamespace
	}
	key := types.NamespacedName{Name: r.Name, Namespace: r.Namespace}
	u := &unstructured.Unstructured{}
	u.SetGroupVersionKind(WorkflowsExecutionGVK)
	if err := reader.Get(ctx, key, u); err != nil {
		if apierrors.IsNotFound(err) {
			return "", k8s.NewReferenceNotFoundError(u.GroupVersionKind(), key)
		}
		return "", fmt.Errorf("reading referenced %s %s: %w", WorkflowsExecutionGVK, key, err)
	}
	// Get external from status.externalRef. This is the most trustworthy place.
	actualExternalRef, _, err := unstructured.NestedString(u.Object, "status", "externalRef")
	if err != nil {
		return "", fmt.Errorf("reading status.externalRef: %w", err)
	}
	if actualExternalRef == "" {
		return "", k8s.NewReferenceNotReadyError(u.GroupVersionKind(), key)
	}
	r.External = actualExternalRef
	return r.External, nil
}

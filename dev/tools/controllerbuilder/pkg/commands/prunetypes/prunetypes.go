package prunetypes

import (
	"context"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/GoogleCloudPlatform/k8s-config-connector/dev/tools/controllerbuilder/pkg/options"
	"github.com/spf13/cobra"
	"k8s.io/klog/v2"
)

type PruneTypesOptions struct {
	*options.GenerateOptions
	Target string
}

func (o *PruneTypesOptions) BindFlags(cmd *cobra.Command) {
	cmd.Flags().StringVar(&o.Target, "target", o.Target, "target file or package directory to prune (e.g. apis/clouddeploy/v1alpha1/types.generated.go)")
}

func BuildCommand(baseOptions *options.GenerateOptions) *cobra.Command {
	opt := &PruneTypesOptions{
		GenerateOptions: baseOptions,
	}

	cmd := &cobra.Command{
		Use:   "prune-types [target]",
		Short: "comment out unreachable types in generated files",
		PreRunE: func(cmd *cobra.Command, args []string) error {
			if len(args) > 0 {
				opt.Target = args[0]
			}
			if opt.Target == "" {
				return fmt.Errorf("target is required (pass file or directory path)")
			}
			return nil
		},
		RunE: func(cmd *cobra.Command, args []string) error {
			ctx := cmd.Context()
			if err := PruneTypes(ctx, opt); err != nil {
				return err
			}
			return nil
		},
	}
	opt.BindFlags(cmd)

	return cmd
}

func isGeneratedFile(filePath string, file *ast.File) bool {
	base := filepath.Base(filePath)
	if strings.Contains(base, ".generated.") || strings.Contains(base, "_generated") || strings.HasPrefix(base, "zz_") {
		return true
	}
	for _, cg := range file.Comments {
		for _, c := range cg.List {
			text := c.Text
			if strings.Contains(text, "//go:build !ignore_autogenerated") ||
				strings.Contains(text, "Code generated by") ||
				strings.Contains(text, "DO NOT EDIT") ||
				strings.Contains(text, "+generated:types") {
				return true
			}
		}
	}
	return false
}

func PruneTypes(ctx context.Context, o *PruneTypesOptions) error {
	stat, err := os.Stat(o.Target)
	if err != nil {
		return fmt.Errorf("stat %s: %w", o.Target, err)
	}

	var pkgDir string
	var targetFiles []string

	if stat.IsDir() {
		pkgDir = o.Target
		// Look for types.generated.go or similar generated files
		entries, err := os.ReadDir(pkgDir)
		if err != nil {
			return fmt.Errorf("reading dir %s: %w", pkgDir, err)
		}
		for _, entry := range entries {
			if entry.IsDir() {
				continue
			}
			name := entry.Name()
			if strings.Contains(name, ".generated.") && strings.HasSuffix(name, ".go") {
				targetFiles = append(targetFiles, filepath.Join(pkgDir, name))
			}
		}
		if len(targetFiles) == 0 {
			return fmt.Errorf("no *.generated.go files found in %s", pkgDir)
		}
	} else {
		pkgDir = filepath.Dir(o.Target)
		targetFiles = []string{o.Target}
	}

	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, pkgDir, nil, parser.ParseComments)
	if err != nil {
		return fmt.Errorf("parsing package %q: %w", pkgDir, err)
	}

	typeToFile := make(map[string]string)
	typeToDecl := make(map[string]*ast.GenDecl)
	typeToSpec := make(map[string]*ast.TypeSpec)
	fileToTypes := make(map[string][]string)
	typeEdges := make(map[string]map[string]bool)
	handwrittenRefs := make(map[string]bool)

	// First pass: discover all types defined in the package
	for _, pkg := range pkgs {
		for filePath, astFile := range pkg.Files {
			for _, decl := range astFile.Decls {
				genDecl, ok := decl.(*ast.GenDecl)
				if !ok || genDecl.Tok != token.TYPE {
					continue
				}
				for _, spec := range genDecl.Specs {
					typeSpec, ok := spec.(*ast.TypeSpec)
					if !ok {
						continue
					}
					typeName := typeSpec.Name.Name
					typeToFile[typeName] = filePath
					typeToDecl[typeName] = genDecl
					typeToSpec[typeName] = typeSpec
					fileToTypes[filePath] = append(fileToTypes[filePath], typeName)
				}
			}
		}
	}

	// Second pass: discover references from handwritten files and edges between types
	for _, pkg := range pkgs {
		for filePath, astFile := range pkg.Files {
			if !isGeneratedFile(filePath, astFile) {
				// Handwritten file: any identifier matching a known type name is a root reference
				ast.Inspect(astFile, func(n ast.Node) bool {
					if ident, ok := n.(*ast.Ident); ok {
						if typeToFile[ident.Name] != "" {
							handwrittenRefs[ident.Name] = true
						}
					}
					return true
				})
			} else {
				// Generated file: inspect each type definition to find outgoing edges to other types
				for _, typeName := range fileToTypes[filePath] {
					spec := typeToSpec[typeName]
					if spec == nil {
						continue
					}
					if typeEdges[typeName] == nil {
						typeEdges[typeName] = make(map[string]bool)
					}
					ast.Inspect(spec, func(n ast.Node) bool {
						if ident, ok := n.(*ast.Ident); ok {
							if ident.Name != typeName && typeToFile[ident.Name] != "" {
								typeEdges[typeName][ident.Name] = true
							}
						}
						return true
					})
				}
			}
		}
	}

	// Compute reachability using BFS
	reachable := make(map[string]bool)
	var queue []string

	for t, ref := range handwrittenRefs {
		if ref {
			reachable[t] = true
			queue = append(queue, t)
		}
	}

	for len(queue) > 0 {
		curr := queue[0]
		queue = queue[1:]

		for dep := range typeEdges[curr] {
			if !reachable[dep] {
				reachable[dep] = true
				queue = append(queue, dep)
			}
		}
	}

	// Prune unreachable types in target files
	type unreachableDecl struct {
		decl     *ast.GenDecl
		typeName string
	}

	for _, targetFile := range targetFiles {
		var toPrune []unreachableDecl
		for _, typeName := range fileToTypes[targetFile] {
			if !reachable[typeName] {
				toPrune = append(toPrune, unreachableDecl{
					decl:     typeToDecl[typeName],
					typeName: typeName,
				})
			}
		}

		if len(toPrune) == 0 {
			klog.Infof("No unreachable types found in %s", targetFile)
			continue
		}

		// Sort descending by offset so modifications from bottom to top don't invalidate earlier offsets
		sort.Slice(toPrune, func(i, j int) bool {
			posI := toPrune[i].decl.Pos()
			if toPrune[i].decl.Doc != nil {
				posI = toPrune[i].decl.Doc.Pos()
			}
			posJ := toPrune[j].decl.Pos()
			if toPrune[j].decl.Doc != nil {
				posJ = toPrune[j].decl.Doc.Pos()
			}
			return fset.Position(posI).Offset > fset.Position(posJ).Offset
		})

		content, err := os.ReadFile(targetFile)
		if err != nil {
			return fmt.Errorf("reading %s: %w", targetFile, err)
		}

		for _, item := range toPrune {
			startPos := item.decl.Pos()
			if item.decl.Doc != nil {
				startPos = item.decl.Doc.Pos()
			}
			endPos := item.decl.End()

			startOffset := fset.Position(startPos).Offset
			endOffset := fset.Position(endPos).Offset

			origText := string(content[startOffset:endOffset])
			cleanText := strings.ReplaceAll(origText, "*/", "* /")
			replacement := fmt.Sprintf("/* unreachable type %s\n%s\n*/", item.typeName, cleanText)

			var newContent []byte
			newContent = append(newContent, content[:startOffset]...)
			newContent = append(newContent, []byte(replacement)...)
			newContent = append(newContent, content[endOffset:]...)
			content = newContent

			klog.Infof("Commented out unreachable type %s in %s", item.typeName, targetFile)
		}

		if err := os.WriteFile(targetFile, content, 0644); err != nil {
			return fmt.Errorf("writing %s: %w", targetFile, err)
		}
	}

	return nil
}

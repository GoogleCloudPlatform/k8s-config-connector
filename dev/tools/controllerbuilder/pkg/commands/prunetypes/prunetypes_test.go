package prunetypes

import (
	"context"
	"os"
	"path/filepath"
	"strings"
	"testing"

	"github.com/GoogleCloudPlatform/k8s-config-connector/dev/tools/controllerbuilder/pkg/options"
	"golang.org/x/tools/go/packages"
)

func TestPruneTypes(t *testing.T) {
	// Create a temporary directory inside the current directory so it is nested
	// under the k8s-config-connector module and can be loaded successfully by go/packages.
	tmpDir, err := os.MkdirTemp(".", "test_temp_*")
	if err != nil {
		t.Fatalf("failed to create temp dir: %v", err)
	}
	defer os.RemoveAll(tmpDir)

	// Extract folder name to use as package name
	pkgName := filepath.Base(tmpDir)

	// Write types.go (handwritten file, references ReachableGeneratedType)
	handwrittenContent := `package ` + pkgName + `

type UsedType struct {
	Ref *ReachableGeneratedType
}
`
	if err := os.WriteFile(filepath.Join(tmpDir, "types.go"), []byte(handwrittenContent), 0644); err != nil {
		t.Fatalf("failed to write types.go: %v", err)
	}

	// Write types.generated.go (generated file, references ReachableChildType)
	generatedContent := `// Code generated by controllerbuilder. DO NOT EDIT.
package ` + pkgName + `

type ReachableGeneratedType struct {
	Value string
	Child *ReachableChildType
}

type ReachableChildType struct {
	Value int
}

type UnreachableGeneratedType struct {
	Value bool
}
`
	generatedFilePath := filepath.Join(tmpDir, "types.generated.go")
	if err := os.WriteFile(generatedFilePath, []byte(generatedContent), 0644); err != nil {
		t.Fatalf("failed to write types.generated.go: %v", err)
	}

	// For debugging, load the package and print errors
	cfg := &packages.Config{
		Mode: packages.NeedName | packages.NeedFiles | packages.NeedCompiledGoFiles | packages.NeedImports | packages.NeedTypes | packages.NeedTypesInfo | packages.NeedSyntax,
		Dir:  tmpDir,
	}
	pkgs, err := packages.Load(cfg, ".")
	if err != nil {
		t.Fatalf("packages.Load failed: %v", err)
	}
	if len(pkgs) > 0 {
		for _, err := range pkgs[0].Errors {
			t.Logf("pkg load error: %v", err)
		}
		t.Logf("loaded pkg: %s with %d syntax files", pkgs[0].PkgPath, len(pkgs[0].Syntax))
	} else {
		t.Logf("no packages loaded")
	}

	// Run PruneTypes
	ctx := context.Background()
	opt := &PruneTypesOptions{
		GenerateOptions: &options.GenerateOptions{},
		Target:          generatedFilePath,
	}

	if err := PruneTypes(ctx, opt); err != nil {
		t.Fatalf("PruneTypes failed: %v", err)
	}

	// Read the pruned file
	prunedBytes, err := os.ReadFile(generatedFilePath)
	if err != nil {
		t.Fatalf("failed to read pruned file: %v", err)
	}
	prunedContent := string(prunedBytes)

	// Assert that UnreachableGeneratedType is commented out
	if !strings.Contains(prunedContent, "/* unreachable type UnreachableGeneratedType") {
		t.Errorf("expected UnreachableGeneratedType to be commented out, got:\n%s", prunedContent)
	}

	// Assert that ReachableGeneratedType and ReachableChildType are NOT commented out
	if strings.Contains(prunedContent, "/* unreachable type ReachableGeneratedType") {
		t.Errorf("expected ReachableGeneratedType to NOT be commented out")
	}
	if strings.Contains(prunedContent, "/* unreachable type ReachableChildType") {
		t.Errorf("expected ReachableChildType to NOT be commented out")
	}
}
